function extractHandle(platform: string, url: string): string {
  try {
    const u = new URL(url);
    const seg = u.pathname.split("/").filter(Boolean)[0] ?? "";
    switch (platform) {
      case "x":
      case "instagram":
      case "github":
        return "@" + seg;
      case "tiktok":
      case "youtube":
        return seg.startsWith("@") ? seg : "@" + seg;
      case "facebook": {
        const segs = u.pathname.split("/").filter(Boolean);
        const last = segs[segs.length - 1] ?? "";
        if (!last || last === "profile.php") {
          const id = u.searchParams.get("id");
          return id ? `id=${id}` : u.hostname + u.pathname;
        }
        return last;
      }
      case "minkara": {
        const segs = u.pathname.split("/").filter(Boolean);
        const idx = segs.indexOf("userid");
        return idx !== -1 && segs[idx + 1] ? segs[idx + 1] : u.hostname + u.pathname;
      }
      case "linkedin": {
        const segs = u.pathname.split("/").filter(Boolean);
        const idx = segs.indexOf("in");
        return idx !== -1 && segs[idx + 1] ? segs[idx + 1] : u.hostname + u.pathname;
      }
      default:
        return u.hostname + u.pathname;
    }
  } catch {
    return url;
  }
}

